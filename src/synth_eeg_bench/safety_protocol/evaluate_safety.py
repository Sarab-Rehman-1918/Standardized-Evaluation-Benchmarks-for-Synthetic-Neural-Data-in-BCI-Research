"""
evaluate_safety.py

Runs the full downstream safety evaluation using real CHB-MIT data
and synthetic segments generated by VAE, GAN, and Diffusion models.

Produces a safety certification report showing:
    - How each generator's synthetic data affects classifier performance
    - Whether synthetic data meets minimum quality thresholds for clinical use
    - False positive rates (critical for patient safety)

Safety thresholds (based on proposal's clinical safety requirements):
    - Accuracy >= 0.75
    - F1 Score >= 0.70
    - False Positive Rate <= 0.20
"""

import sys
import os
import random
import numpy as np

sys.path.append(os.path.join(os.path.dirname(__file__), "..", ".."))

from synth_eeg_bench.preprocessing.load_epilepsy_chbmit import load_subject
from synth_eeg_bench.safety_protocol.train_downstream_classifier import (
    run_safety_protocol,
)
from synth_eeg_bench.generators.vae_baseline import (
    train_vae,
    generate_synthetic_segments as vae_generate,
)
from synth_eeg_bench.generators.gan_baseline import (
    train_gan,
    generate_synthetic_segments as gan_generate,
)
from synth_eeg_bench.generators.diffusion_baseline import (
    train_diffusion,
    generate_synthetic_segments as diffusion_generate,
)


# ----------------------------------------------------------------------
# Safety thresholds
# ----------------------------------------------------------------------

SAFETY_THRESHOLDS = {
    "accuracy": 0.75,
    "f1_score": 0.70,
    "false_positive_rate": 0.20,  # must be BELOW this
}


# ----------------------------------------------------------------------
# 1. Check if a generator passes safety thresholds
# ----------------------------------------------------------------------

def check_safety_thresholds(metrics):
    """
    Checks whether a set of evaluation metrics meets minimum safety thresholds.

    Parameters:
        metrics (dict): output from evaluate_classifier

    Returns:
        dict: {metric_name: {"value": float, "passed": bool}}
    """
    results = {}

    results["accuracy"] = {
        "value": metrics["accuracy"],
        "passed": metrics["accuracy"] >= SAFETY_THRESHOLDS["accuracy"],
    }
    results["f1_score"] = {
        "value": metrics["f1_score"],
        "passed": metrics["f1_score"] >= SAFETY_THRESHOLDS["f1_score"],
    }
    results["false_positive_rate"] = {
        "value": metrics["false_positive_rate"],
        "passed": metrics["false_positive_rate"] <= SAFETY_THRESHOLDS["false_positive_rate"],
    }

    results["overall_passed"] = all(v["passed"] for v in results.values() if isinstance(v, dict))

    return results


# ----------------------------------------------------------------------
# 2. Print Safety Report for one generator
# ----------------------------------------------------------------------

def print_safety_report(generator_name, condition_results, threshold_results):
    """
    Prints a formatted safety certification report for one generator.
    """
    print(f"\n{'=' * 60}")
    print(f"SAFETY REPORT: {generator_name.upper()}")
    print(f"{'=' * 60}")

    for condition, metrics in condition_results.items():
        print(f"\n  Condition: {condition.upper()}")
        print(f"    Accuracy:            {metrics['accuracy']:.4f}")
        print(f"    F1 Score:            {metrics['f1_score']:.4f}")
        print(f"    False Positive Rate: {metrics['false_positive_rate']:.4f}")

    print(f"\n  Threshold Check (synthetic_only condition):")
    for metric, result in threshold_results.items():
        if metric == "overall_passed":
            continue
        status = "PASS" if result["passed"] else "FAIL"
        print(f"    {metric:25s}: {result['value']:.4f} [{status}]")

    overall = threshold_results.get("overall_passed", False)
    verdict = "CERTIFIED SAFE" if overall else "NOT CERTIFIED"
    print(f"\n  Overall Verdict: {verdict}")
    print(f"{'=' * 60}")


# ----------------------------------------------------------------------
# 3. Full Safety Evaluation Pipeline
# ----------------------------------------------------------------------

def run_full_safety_evaluation():
    """
    Runs the complete safety evaluation pipeline:
        1. Load real CHB-MIT data
        2. Train each generator on real interictal data
        3. Generate synthetic interictal + use real ictal for labeling
        4. Run safety protocol for each generator
        5. Print safety certification report
    """

    CHB01_DIR = "data/raw/epilepsy/chb_mit/chb01"
    CHB01_SUMMARY = "data/raw/epilepsy/chb_mit/chb01/chb01-summary.txt"
    CHB03_DIR = "data/raw/epilepsy/chb_mit/chb03"
    CHB03_SUMMARY = "data/raw/epilepsy/chb_mit/chb03/chb03-summary.txt"

    print("Loading real CHB-MIT segments...")
    chb01_segments = load_subject(CHB01_DIR, CHB01_SUMMARY, segment_length_sec=10)
    chb03_segments = load_subject(CHB03_DIR, CHB03_SUMMARY, segment_length_sec=10)

    all_segments = chb01_segments + chb03_segments
    sfreq = all_segments[0]["sfreq"]

    real_interictal = [s["data"] for s in all_segments if s["label"] == "interictal"]
    real_ictal = [s["data"] for s in all_segments if s["label"] == "ictal"]

    print(f"Real interictal: {len(real_interictal)} | Real ictal: {len(real_ictal)}")

    # subsample for speed
    random.seed(42)
    train_pool = random.sample(real_interictal, min(150, len(real_interictal)))
    real_segments = random.sample(real_interictal, min(100, len(real_interictal))) + real_ictal
    real_labels = [0] * min(100, len(real_interictal)) + [1] * len(real_ictal)

    segment_shape = real_interictal[0].shape

    # ------------------------------------------------------------------
    # Generator 1: VAE
    # ------------------------------------------------------------------
    print("\n--- Training VAE ---")
    vae_model, vae_mean, vae_std, vae_shape = train_vae(
        train_pool, latent_dim=64, epochs=20, batch_size=16, learning_rate=1e-4
    )
    vae_synthetic = vae_generate(vae_model, vae_mean, vae_std, vae_shape, n_samples=80)
    vae_synth_labels = [0] * 80

    vae_results = run_safety_protocol(
        real_segments, real_labels,
        vae_synthetic, vae_synth_labels,
        sfreq,
    )
    vae_thresholds = check_safety_thresholds(vae_results["synthetic_only"])
    print_safety_report("VAE", vae_results, vae_thresholds)

    # ------------------------------------------------------------------
    # Generator 2: GAN
    # ------------------------------------------------------------------
    print("\n--- Training GAN ---")
    gan_gen, gan_mean, gan_std, gan_shape = train_gan(
        train_pool, latent_dim=64, epochs=30, batch_size=16, learning_rate=2e-4
    )
    gan_synthetic = gan_generate(gan_gen, gan_mean, gan_std, gan_shape, n_samples=80)
    gan_synth_labels = [0] * 80

    gan_results = run_safety_protocol(
        real_segments, real_labels,
        gan_synthetic, gan_synth_labels,
        sfreq,
    )
    gan_thresholds = check_safety_thresholds(gan_results["synthetic_only"])
    print_safety_report("GAN", gan_results, gan_thresholds)

    # ------------------------------------------------------------------
    # Generator 3: Diffusion
    # ------------------------------------------------------------------
    print("\n--- Training Diffusion Model ---")
    diff_model, diff_schedule, diff_mean, diff_std, diff_shape = train_diffusion(
        train_pool, timesteps=100, epochs=20, batch_size=16, learning_rate=1e-3
    )
    diff_synthetic = diffusion_generate(
        diff_model, diff_schedule, diff_mean, diff_std, diff_shape,
        n_samples=80, timesteps=100
    )
    diff_synth_labels = [0] * 80

    diff_results = run_safety_protocol(
        real_segments, real_labels,
        diff_synthetic, diff_synth_labels,
        sfreq,
    )
    diff_thresholds = check_safety_thresholds(diff_results["synthetic_only"])
    print_safety_report("Diffusion", diff_results, diff_thresholds)

    return {
        "vae": {"results": vae_results, "thresholds": vae_thresholds},
        "gan": {"results": gan_results, "thresholds": gan_thresholds},
        "diffusion": {"results": diff_results, "thresholds": diff_thresholds},
    }


# ----------------------------------------------------------------------
# 4. Entry point
# ----------------------------------------------------------------------

if __name__ == "__main__":
    all_results = run_full_safety_evaluation()